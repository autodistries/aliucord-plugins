            }
        commands.registerCommand("smaller", "Makes your text more more small ([a-z])", listOf(CommandsAPI.requiredMessageOption))   { ctx ->
            CommandsAPI.CommandResult(smallerify(ctx.getRequiredString("message").trim()))
        }


    }



